package entity;

public class Temperature {
    private double degreeInC;

    public Temperature(double degreeInC) {
        this.degreeInC = degreeInC;
    }

    public double getDegreeInC() {
        return degreeInC;
    }

    public void setDegreeInC(double degreeInC) {
        this.degreeInC = degreeInC;
    }
}


